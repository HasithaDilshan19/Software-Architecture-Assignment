public class Van {
    int no;
    String Name;

    public Van() {
    }

    public Van(int no, String name) {
        this.no = no;
        Name = name;
    }

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
